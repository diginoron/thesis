          <li className="mb-2"><strong>کدگذاری مجدد داده‌ها:</strong> متغیرهای معکوس و نحوه محاسبه نمره کل سازه‌ها (در مدل‌های Path Analysis) باید پیش از محیط ایموس تنظیم شوند.</li>
